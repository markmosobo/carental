<script>
import L from 'leaflet';
import 'leaflet-routing-machine';

export default {
  name: 'MapPicker',
  data() {
    return {
      showMap: false,
      map: null,
      selectedLocation: '',
      marker: null,

      showDropoffMap: false,
      dropoffMap: null,
      dropoffMarker: null,
      selectedDropoffLocation: '',

      showRouteMap: false,
      mapInstance: null  
    };
  },
  watch: {
    showMap(val) {
      if (val) {
        this.$nextTick(this.initMap);
      }
    },
    showDropoffMap(val) {
    if (val) {
      this.$nextTick(this.initDropoffMap);
    }
  },
  },
  methods: {
    initMap() {
      if (!this.map) {
        this.map = L.map(this.$refs.mapContainer).setView([-1.286389, 36.817223], 12); // Nairobi default
        L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
          attribution: '¬© OpenStreetMap contributors',
        }).addTo(this.map);

        this.map.on('click', (e) => {
          const { lat, lng } = e.latlng;
          this.selectedLocation = `${lat.toFixed(5)}, ${lng.toFixed(5)}`;

          if (this.marker) {
            this.map.removeLayer(this.marker);
          }

          this.marker = L.marker([lat, lng]).addTo(this.map);
        });
      } else {
        this.map.invalidateSize(); // Fix layout if map was hidden
      }
    },
    initDropoffMap() {
    if (!this.dropoffMap) {
      this.dropoffMap = L.map(this.$refs.dropoffMapContainer).setView([-1.286389, 36.817223], 12);
      L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        attribution: '¬© OpenStreetMap contributors',
      }).addTo(this.dropoffMap);

      this.dropoffMap.on('click', (e) => {
        const { lat, lng } = e.latlng;
        this.selectedDropoffLocation = `${lat.toFixed(5)}, ${lng.toFixed(5)}`;

        if (this.dropoffMarker) {
          this.dropoffMap.removeLayer(this.dropoffMarker);
        }

        this.dropoffMarker = L.marker([lat, lng]).addTo(this.dropoffMap);
      });
    } else {
      this.dropoffMap.invalidateSize();
    }
    },
    async handleProceed() {
    const carousel = new bootstrap.Carousel(this.$refs.carousel);
    carousel.next();

    this.showRouteMap = true;

    await this.$nextTick();

    // Small timeout to allow carousel transition to finish
    setTimeout(() => {
        this.displayRouteMap();
    }, 500); // you may increase if your carousel transition takes longer
    },


    displayRouteMap() {
  if (this.selectedLocation && this.selectedDropoffLocation) {
    const pickupCoords = [
      this.selectedLocation.lat || this.selectedLocation[0],
      this.selectedLocation.lng || this.selectedLocation[1],
    ];
    const dropoffCoords = [
      this.selectedDropoffLocation.lat || this.selectedDropoffLocation[0],
      this.selectedDropoffLocation.lng || this.selectedDropoffLocation[1],
    ];

    if (this.mapInstance) {
      this.mapInstance.remove();
    }

    this.mapInstance = L.map(this.$refs.routeMap).setView(pickupCoords, 13);

    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
      attribution: '¬© OpenStreetMap contributors',
    }).addTo(this.mapInstance);

    L.Routing.control({
      waypoints: [
        L.latLng(...pickupCoords),
        L.latLng(...dropoffCoords),
      ],
      routeWhileDragging: false,
    }).addTo(this.mapInstance);
  }
},

    getPhoto() {
      return '/storage/assets/';
    },
  },
};
</script>

